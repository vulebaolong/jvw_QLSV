    private void btnCreateTeacherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateTeacherActionPerformed
        try {
            // üîπ 1. Ki·ªÉm tra th√¥ng tin nh·∫≠p v√†o
            String email = txtEmail.getText().trim();
            String password = new String(txtPassword.getPassword()).trim();

            if (email.isEmpty()) {
                Toast.show("‚ùå Email kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!");
                txtEmail.requestFocus();
                return;
            }

            if (password.isEmpty()) {
                Toast.show("‚ùå M·∫≠t kh·∫©u kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!");
                txtPassword.requestFocus();
                return;
            }

            String fullName = txtFullname.getText().trim();
            String gender = cbbGender.getSelectedItem().toString();
            String phone = txtPhone.getText().trim();
            String address = txtAddress.getText().trim();

            Date birthDate = null;
            java.util.Date selectedDate = dateBirthDay.getDate();
            if (selectedDate != null) {
                birthDate = new Date(selectedDate.getTime()); // Chuy·ªÉn th√†nh java.sql.Date
            } else {
                Toast.show("‚ùå Vui l√≤ng ch·ªçn ng√†y sinh!");
                return;
            }

            if (fullName.isEmpty() || phone.isEmpty() || address.isEmpty()) {
                Toast.show("‚ùå Vui l√≤ng ƒëi·ªÅn ƒë·∫ßy ƒë·ªß th√¥ng tin!");
                return;
            }

            // üîπ 2. Ki·ªÉm tra n·∫øu email ƒë√£ t·ªìn t·∫°i
            User userExists = userDAO.getUserByEmail(email);
            if (userExists != null) {
                Toast.show("‚ùå Email ƒë√£ t·ªìn t·∫°i!");
                txtEmail.requestFocus();
                return;
            }

            // üîπ 3. T·∫°o ƒë·ªëi t∆∞·ª£ng User v√† Teacher
            User user = new User(email, BCrypt.hashpw(password, BCrypt.gensalt(10)));
            Teacher teacher = new Teacher(fullName, birthDate, gender, phone, address);

            // üîπ 4. G·ªçi DAO ƒë·ªÉ th·ª±c hi·ªán transaction
            boolean success = teacherDAO.addTeacherWithTransaction(user, teacher);

            if (success) {
                Toast.show("‚úÖ Th√™m gi·∫£ng vi√™n th√†nh c√¥ng!");

                // üîπ 5. C·∫≠p nh·∫≠t giao di·ªán
                if (adminPanel != null) {
                    adminPanel.reloadDataAdmin();
                }

                this.dispose();
            } else {
                Toast.show("‚ùå Th√™m gi·∫£ng vi√™n th·∫•t b·∫°i!");
            }

        } catch (IllegalArgumentException e) {
            Toast.show("‚ùå Ng√†y sinh kh√¥ng h·ª£p l·ªá!");
        }
    }//GEN-LAST:event_btnCreateTeacherActionPerformed

